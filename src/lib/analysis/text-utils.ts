export function tokenize(text: string): string[] {
  return text
    .toLowerCase()
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/`[^`]+`/g, " ")
    .replace(/https?:\/\/\S+/g, " ")
    .replace(/[^a-zäöüß\s'-]/g, " ")
    .split(/\s+/)
    .filter((w) => w.length > 1);
}

function stemEN(word: string): string {
  if (word.length < 5) return word;
  if (word.endsWith("tion")) return word.slice(0, -4);
  if (word.endsWith("ness")) return word.slice(0, -4);
  if (word.endsWith("ment")) return word.slice(0, -4);
  if (word.endsWith("able")) return word.slice(0, -4);
  if (word.endsWith("ible")) return word.slice(0, -4);
  if (word.endsWith("ally")) return word.slice(0, -4);
  if (word.endsWith("ing") && word.length > 5) return word.slice(0, -3);
  if (word.endsWith("ful")) return word.slice(0, -3);
  if (word.endsWith("ous")) return word.slice(0, -3);
  if (word.endsWith("ive")) return word.slice(0, -3);
  if (word.endsWith("ed") && word.length > 4) return word.slice(0, -2);
  if (word.endsWith("ly") && word.length > 4) return word.slice(0, -2);
  if (word.endsWith("er") && word.length > 4) return word.slice(0, -2);
  if (word.endsWith("es") && word.length > 4) return word.slice(0, -2);
  if (word.endsWith("est") && word.length > 5) return word.slice(0, -3);
  if (word.endsWith("s") && !word.endsWith("ss") && word.length > 4) return word.slice(0, -1);
  return word;
}

function stemDE(word: string): string {
  if (word.length < 5) return word;
  if (word.endsWith("ung")) return word.slice(0, -3);
  if (word.endsWith("heit")) return word.slice(0, -4);
  if (word.endsWith("keit")) return word.slice(0, -4);
  if (word.endsWith("lich")) return word.slice(0, -4);
  if (word.endsWith("isch")) return word.slice(0, -4);
  if (word.endsWith("ieren")) return word.slice(0, -5);
  if (word.endsWith("ig") && word.length > 4) return word.slice(0, -2);
  if (word.endsWith("en") && word.length > 4) return word.slice(0, -2);
  if (word.endsWith("er") && word.length > 4) return word.slice(0, -2);
  return word;
}

export function getCleanTokens(text: string, language: "en" | "de" | "mixed"): string[] {
  const words = tokenize(text);
  const stopwords =
    language === "de"
      ? DE_STOPWORDS
      : language === "en"
        ? EN_STOPWORDS
        : new Set([...EN_STOPWORDS, ...DE_STOPWORDS]);
  const stemFn = language === "de" ? stemDE : stemEN;

  return words.filter((w) => !stopwords.has(w) && w.length > 2).map((w) => stemFn(w));
}

